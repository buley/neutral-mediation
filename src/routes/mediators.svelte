	<h3>Who are mediators?</h3>
	
	<p>Mediators are craftsmen at the art of compromise. They settle disputes with expertise, resolve and creativity.</p>
	
	<p>Their expertise is typically derived from training and practice. They are not licensed or regulated and. </p>
	
	<p>They are not admissible witnesses in most courts and strive to be:</p>
	
	<ul>
		<li>Empathetic</li>
		<li>Understanding</li>
		<li>Non-judgmental</li>
		<li>Validating of feelings but not facts</li>
		<li>Honest</li>
		<li>Accepting</li>
	</ul>
	
	<h3>What does a mediator do?</h3>

	
	<p>A mediator fixes problems through "The 3 E's" — education, empathy and empowerment.</p>

	
	<h4>Empathy</h4>

	
	<h4>Education</h4>

	
	<p>Support communication/draw out information</p>
	
	
	<p>Explore/inform parties of risks of litigation. Remind parties of costs of continuing the dispute.</p>
	
	
	<p>Encourage flexibility about offers, explain tactics</p>
	
	
	<p>Seek non-monetary options</p>
	
	
