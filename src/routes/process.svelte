	<p>Signal</p>

	<ul><li>Size of concession</li><li>Amount of time between concessions<ul><li>Larger earlier</li><li>Smaller as time goes on</li></ul></li><li>Help the people understand about the message they’re trying to send.&nbsp;<ul><li>Ask what message want to send</li><li>Ask what message they think other side will see</li><li>Stating how they would react or interpret</li></ul></li><li>Manage the amount of time between offers</li></ul>

	<p>Unless for physical injury, settlement is taxable.</p>

	<p>Reach closure faster in time sensitive matters without expensive and time consuming litigation.</p>

	<h4>Questions</h4>

	<p>Types of questions</p>

	<ul><li>Closed</li><li>Open-ended</li><li>Leading</li><li>Active listening</li><li>Elaboration</li><li>Clarification</li><li>Confirmation</li><li>Confrontation</li><li>Hypothetical</li></ul>

	<p>Ask questions for the purpose of communication</p>

	<ul><li>Amplify certain aspects of the message that he thinks is constructive to reaching an agreement</li><li>Learn and share information</li></ul>

	<p>Expresses judgement and style in which questions to choose</p>

	<p>Caucus</p>

	<ul><li>“Shuttle diplomacy”<ul><li>Not in legal proceedings due to due process</li><li>All information is symmetrical</li><li>Only in mediation can we use this asymmetric information?</li><li>maturity to recognize good evidence from bad evidence and not be manipulated</li><li>Not let the private information influence process or&nbsp;</li></ul></li><li>Types<ul><li>Initial caucus<ul><li>Anything I need to know?</li><li>Express anger, be vulnerable</li><li>Hidden obstacles</li><li>Maybe an opening offer</li></ul></li><li>Later caucuses</li></ul></li><li>Initial Process<ul><li>Open-ended questions</li><li>Elicit discussion between parties</li><li>Listen carefully</li><li>Acknowledge, emphasize</li><li>Summarize but don’t evaluate</li><li>Translate or reframe comments</li></ul></li><li>Later process<ul><li>Translate perspectives, filter negative information</li><li>Reality test<ul><li>Convey data and test</li><li>Question assumptions</li><li>Play worst case scenario</li></ul></li><li>Promote bargaining: what ifs etc</li><li>Generate options</li><li>Explore potential settlement packages</li><li>If necessary<ul><li>Evaluate legal issues</li><li>Encourage entry of new people</li><li>Propose settlement terms</li><li>Adjourn and follow up</li></ul></li></ul></li><li>Private caucus<ul><li>Greater candor/intimacy between participant and the mediator</li><li>Caucus and coach<ul><li>Will you be willing to share with the other side?</li><li>I was hoping you’d be willing to share...</li><li>Coach the speaker, and sometimes listener, too</li></ul></li></ul></li><li>Process<ul><li>Confidentiality promise<ul><li>Explicit permission</li><li>Presumed permission (everything except what tell me not to)</li><li>Carte Blanche (use my own judgement?)</li></ul></li><li>Open-ended questions</li><li>Probe for deeper understanding of how they plan to prove feelings and value of case</li><li>Probe for underlying intent</li></ul></li></ul>

	<h3>Negotiation</h3>

	<p>Encourage parties to test eachotherDiscourage counterproductive offers</p>

	<ul><li>Emphasize&nbsp; the&nbsp; danger&nbsp; of&nbsp; an&nbsp; extreme&nbsp; offer&nbsp; preventing&nbsp; the&nbsp; starting&nbsp; of&nbsp; the real distributive dance.</li></ul>

	<p>Normalizing the dance</p>

	<ul><li>People get frustrated by the back and forth.&nbsp;</li><li>A mediator can acknowledge the frustration and explain that the dance is just part of the process.</li><li>Explain is no right and wrong.</li></ul>

	<p>Extracting concessions</p>

	<ul><li>Can keep track of who’s turn it is to move and make the next concession</li><li>Compare and contrast to “what’s reasonable” based on experience</li></ul>

	<p>Confidential listener</p>

	<ul><li>Figure out, when people are way apart, what are their real goals in a positional distributive bargaining process&nbsp;</li><li>Meanwhile, protect bargaining position and ability to bluff and maneuvers</li><li>When? Bargaining can be frustrating and stalling.</li><li>Technique<ul><li>Tell me your next-to-last offer</li><li>Explain in words a “significant gap” and&nbsp;</li></ul></li></ul>

	<p>Help parties maintain credibility and save face</p>

	<p>Uses</p>

	<ul><li>Anchoring</li><li>Signaling</li><li>Anger</li><li>Wish&nbsp;</li><li>Misunderstanding</li><li>Disagreement</li></ul>

	<p>Aspects</p>

	<ul><li>Organization/discussion</li></ul>

	<p>The mediator is a negotiation doctor</p>

	<p>Creative problem-solving.</p>

	<p>Coaching</p>

	<ul><li>Question: message, impact<ul><li>Ask them what message they want to send?</li><li>Explain what the impact would be on others</li><li>Where do you want to be at the end of the day and how is that going to get you there?</li></ul></li><li>Coach: explain or offer a better offer<ul><li>Evaluative: I’m concerned because people who make an offer that way tend to see X as a response and get boxed in.</li><li>What ‘s the reaction you want and is this the best way to accomplish?</li></ul></li><li>Bargain<ul><li>What can you ad?</li><li>Can you push them?</li><li>Add sweeteners</li><li>Ask for a more reasonable offer</li></ul></li><li>Pocket (keep private)<ul><li>Change focus</li><li>Facts</li></ul></li><li>Present it with context</li></ul>

	<h3>Closing</h3>

	<p>Hard sell vs. consultative selling. During closing phase, mediator runs the highest risk of being a bully.</p>

	<p>The mediator becomes the salesperson. Someone needs to say the resolution is better than continued fighting. Advocate for resolution.</p>

	<p>A small portion of time, but a large part of the value of what people pay you to do.</p>

	<p>Outcome:</p>

	<ul><li>Willingness</li><li>Safety and hope</li><li>Expression and understanding</li><li>Flexibility and innovation</li><li>Informed Decision</li></ul>

	<p>Role of mediator</p>

	<ul><li>Mediator’s proposal<ul><li>Ask permission from both sides?</li><li>Point of mutual pain</li><li>Ask if they accepted if you would accept it</li><li>If you both say yes, you have a deal. If either says no, neither side finds out that the other said yes.</li><li>When?<ul><li>Exhausted analysis</li><li>No way to restart</li><li>Explored interests without success</li><li>Waited to see if parties would come up with something</li></ul></li><li>“Jump to an agreement” ends back and forth torture.&nbsp;</li><li>Could change expectations on what can get even if doesn’t work</li><li>Sometimes a negotiated mediator’s proposal</li></ul></li></ul>

	<h4>Barriers to agreement</h4>

	<ul><li>Lack of information<ul><li>Encourage exchange/discovery</li><li>Suggest oral representations (vs. whitepapers) with a right of verification on any fact</li><li>Take advantage of confidentiality<ul><li>“Confidential mediation document”</li><li>Name</li><li>Date&nbsp;</li></ul></li><li>Suggest a neutral analysis</li></ul></li><li>Disagreement over legal analysis<ul><li>Ask questions</li><li>Lead analysis<ul><li>Explain hurdles<ul><li>Plaintiff (6% chance):<ul><li>Prove Liability</li><li>Affirmative Defenses</li><li>Prove Damages</li><li>Collection of Damanges</li></ul></li><li>Cost of litigation</li><li>What juries like X or judges like Y are doing</li></ul></li></ul></li></ul></li><li>Mistrust<ul><li>Promote confidence-building and certainty</li><li>Reframe risks</li></ul></li><li>Format limitations<ul><li>Time limits</li><li>Parallel discussion tracks</li></ul></li><li>Lack of interest-based terms<ul><li>It’s hard to get interested-based terms when the relationship is ending.</li><li>Complex deals take time</li></ul></li><li>Cognitive reasons<ul><li>Loss aversion<ul><li>Losses count 4x gains</li><li>Framing matters; can reframe</li></ul></li><li>Judgmental overconfidence<ul><li>Distance the rider from the horse</li></ul></li></ul></li><li>Emotional reasons<ul><li>Moral/symbolic</li><li>Emotional</li></ul></li></ul>
