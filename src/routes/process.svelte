	<h3>Information Exchange</h3>

	<p>Helping these folks understand each other</p>

	<p>Doesn’t have as much emphasis in the Pepperdine system</p>

	<ul><li>The birth of the conflict is not a misunderstanding&nbsp;</li><li>Sometimes, more understanding is more repulsion</li><li>Honor the communication stage, but do not bank on it</li></ul>

	<p>Easy to get them to share their story. Hard to get people to understand each other.</p>

	<p>Communication and negotiation go together. Eats 70-80% of the time.</p>

	<p>Validation</p>

	<ul><li>acknowledgment of another's existence and importance, assurance that feelings and expressions are being recognized and valued.</li><li>listener must communicate that he will accept and value what is being communicated</li></ul>

	<h4>Communication</h4>

	<p>What are you looking for in the communication phase? Information related to the issues. Lead them toward resolution. Discovering information they need to make a decision. And also what the arbitrator needs to know. Emphasizing the parts that need to be heard.</p>

	<p>Pacing is very important. Have to hold people in the phase as long as necessary. The longer you can hold them into the Communication phase the more they go from combative to collaborative.&nbsp; Want to keep them out of the negotiation phase as long as you can. Slowing down to speed up.</p>

	<p>Mediators and participants communicate via words, intonation and body language. </p>

